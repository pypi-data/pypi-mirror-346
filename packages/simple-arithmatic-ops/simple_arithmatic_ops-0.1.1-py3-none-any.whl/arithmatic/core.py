"""
Core arithmetic operations for integers.
"""

def add(a: int, b: int) -> int:
    """
    Add two integers.
    
    Args:
        a (int): First integer
        b (int): Second integer
        
    Returns:
        int: Sum of a and b
    """
    return a + b

def subtract(a: int, b: int) -> int:
    """
    Subtract two integers.
    
    Args:
        a (int): First integer
        b (int): Second integer
        
    Returns:
        int: Difference of a and b
    """
    return a - b 