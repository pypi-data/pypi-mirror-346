def _load_environment():
    """
    Load .env file from project tree or home directory (silent if not found).
    """
    path = find_dotenv(usecwd=True)
    if path:
        load_dotenv(path, override=True)
    else:
        home = Path.home() / '.env'
        if home.is_file():
            load_dotenv(home, override=True)

