def connector():  # -> IBMBackend
    key,name,human = _get_plan()
    _load_environment()
    with warnings.catch_warnings():
        warnings.filterwarnings('ignore',category=DeprecationWarning)
        service = QiskitRuntimeService()
    print(HEADER_LINE)
    print(f"\nâš›ï¸ Connecting ({human}) to least-busy QPU...")
    print(SUBHEADER_LINE)
    if key=='open':
        backend = service.least_busy(
            simulator=False,
            operational=True,
            min_num_qubits=5)
    else:
        cred = _get_credentials(key)
        backend = service.least_busy(
            simulator=False, 
            operational=True,
            instance=cred['instance'],
            min_num_qubits=5
        )
    if not backend:
        raise RuntimeError(f"â›” No QPU available for {human}")
    
    # list all available QPUs under this plan
    qpus = service.backends(
           simulator=False, 
           operational=True,
           min_num_qubits=5,
    )
    print(f"âš›ï¸ Connected [{human}] -> Realtime Least Busy QPU:: [{backend.name}]")
    for q in qpus:
        print(f"- {q.name}")
    print("\n")
    print(f"ğŸ–¥ï¸ Least Busy QPU Now: [{backend.name}]")
    print(f"ğŸ–¥ï¸ Version: {getattr(backend,'version','N/A')}")
    print(f"ğŸ–¥ï¸ Qubits Count: {getattr(backend,'num_qubits','N/A')}")
    print(f"ğŸ–¥ï¸ Backend [{backend.name}] ready for use: Yes")
    print(HEADER_LINE + "\n")
    return backend

